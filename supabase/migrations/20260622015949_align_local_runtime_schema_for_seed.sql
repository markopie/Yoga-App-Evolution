-- Align the local schema with the app runtime data exported from the hosted project.
-- This intentionally does not recreate obsolete/source-analysis tables from the cloud dump.

create table if not exists public.course_categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  created_at timestamp with time zone default now()
);

create table if not exists public.course_sub_categories (
  id bigint generated by default as identity primary key,
  category_id bigint not null references public.course_categories(id) on delete cascade,
  name text not null,
  unique (category_id, name)
);

