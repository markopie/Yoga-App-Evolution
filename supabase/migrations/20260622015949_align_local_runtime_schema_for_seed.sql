create or replace view public.view_asanas_admin as
select
  a.id,
  a.name,
  a.iast,
  a.english_name,
  a.technique,
  a.description,
  ac.name as category,
  a.category_id,
  a.gem_plate,
  a.plate_numbers,
  a.hold_json,
  a.page_primary,
  a.requires_sides,
  a.preparatory_pose_id,
  a.recovery_pose_id,
  a.audio_url,
  a.image_url,
  a.intensity,
  a.is_system,
  a.is_curated,
  null::uuid as user_id
from public.asanas a
left join public.asana_categories ac on ac.id = a.category_id;

grant select on public.view_asanas_admin to authenticated;

create or replace view public.searchable_asanas_view as
select
  a.id as source_id,
  a.id as asana_id,
  a.english_name as display_name,
  a.iast,
  a.name,
  ac.name as category,
  a.image_url,
  a.page_primary,
  null::text as stage_name,
  null::text as stage_title,
  null::text as stage_shorthand,
  'asana' as source_type
from public.asanas a
left join public.asana_categories ac on ac.id = a.category_id

union all

select
  s.id::text as source_id,
  s.asana_id as asana_id,
  coalesce(s.title, a.english_name) as display_name,
  s.devanagari as iast,
  coalesce(s.title, a.name) as name,
  ac.name as category,
  coalesce(s.image_url, a.image_url) as image_url,
  s.page_primary,
  s.stage_name,
  s.title as stage_title,
  s.shorthand as stage_shorthand,
  'stage' as source_type
from public.stages s
left join public.asanas a on a.id = s.asana_id
left join public.asana_categories ac on ac.id = a.category_id;

grant select on public.searchable_asanas_view to anon, authenticated;

